#import "UIColor+SGColors.h"

@implementation UIColor (SGColors)
+ (UIColor *)SGRedColor
{
    return [UIColor colorWithRed:0.98 green:0 blue:0.231 alpha:1.0];
}


+ (UIColor *)SGBlueColor
{
    return [UIColor colorWithRed:0.039 green:0.082 blue:0.18 alpha:1.0];
}

+ (UIColor *)SGTransparentBlueColor
{
    return [UIColor colorWithRed:0.039 green:0.082 blue:0.18 alpha:0.5];
}

@end
